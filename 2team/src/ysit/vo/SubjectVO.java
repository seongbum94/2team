package ysit.vo;

/**
 * 전체 과목 정보를 저장하는 VO 
 *
 */

public class SubjectVO {
	private String sub_id;			// 과목코드[PK]
	private String sub_name;		// 과목명
	private String sub_year;		// 학년
	
	
	// getter, setter
	public String getSub_id() {
		return sub_id;
	}
	public void setSub_id(String sub_id) {
		this.sub_id = sub_id;
	}
	public String getSub_name() {
		return sub_name;
	}
	public void setSub_name(String sub_name) {
		this.sub_name = sub_name;
	}
	public String getSub_year() {
		return sub_year;
	}
	public void setSub_year(String sub_year) {
		this.sub_year = sub_year;
	}
	
	// toString
	@Override
	public String toString() {
		return "SubjectVO [sub_id=" + sub_id + ", sub_name=" + sub_name + ", sub_year=" + sub_year + ", getSub_id()="
				+ getSub_id() + ", getSub_name()=" + getSub_name() + ", getSub_year()=" + getSub_year()
				+ ", getClass()=" + getClass() + ", hashCode()=" + hashCode() + ", toString()=" + super.toString()
				+ "]";
	}
	
	

}
