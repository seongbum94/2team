package ysit.vo;

/**
 * 강의시간 정보를 저장하는 VO 
 */

public class LectureTimeVO {
	private String opsub_id;			// 개설코드[PK,FK]
	private String sub_day_week;		// 요일[PK]
	private String sub_period;			// 교시[PK]
	
	// getter, setter
	public String getOpsub_id() {
		return opsub_id;
	}
	public void setOpsub_id(String opsub_id) {
		this.opsub_id = opsub_id;
	}
	public String getSub_day_week() {
		return sub_day_week;
	}
	public void setSub_day_week(String sub_day_week) {
		this.sub_day_week = sub_day_week;
	}
	public String getSub_period() {
		return sub_period;
	}
	public void setSub_period(String sub_period) {
		this.sub_period = sub_period;
	}
	
	// toString
	@Override
	public String toString() {
		return "LectureTimeVO [opsub_id=" + opsub_id + ", sub_day_week=" + sub_day_week + ", sub_period=" + sub_period
				+ "]";
	}
	
	

}
