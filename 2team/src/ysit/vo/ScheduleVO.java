
package ysit.vo;

/**
 * 학사일정 정보를 저장하는 VO
 *
 */

public class ScheduleVO {
	private String sch_date;		// 학사일정 날짜[PK]
	private String admin_id;		// 관리자사번[FK]
	private String sch_content;		// 일정내용
	
	
	
	// getter, setter
	public String getSch_date() {
		return sch_date;
	}
	public void setSch_date(String sch_date) {
		this.sch_date = sch_date;
	}
	public String getAdmin_id() {
		return admin_id;
	}
	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}
	public String getSch_content() {
		return sch_content;
	}
	public void setSch_content(String sch_content) {
		this.sch_content = sch_content;
	}
	
	// toString
	@Override
	public String toString() {
		return "ScheduleVO [sch_date=" + sch_date + ", admin_id=" + admin_id + ", sch_content=" + sch_content + "]";
	}

}

