package ysit.vo;

/**
 * 관리자 정보를 저장하는 VO 
 *
 */

public class AdminVO {
	private String admin_id;			// 사번(관리자 아이디) [PK]
	private String admin_pass;			// 비밀번호
	private String admin_name;			// 관리자 이름
	private int admin_grant;			// 권한 (1)
	

	// getter, setter
	public String getAdmin_id() {
		return admin_id;
	}
	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}
	public String getAdmin_pass() {
		return admin_pass;
	}
	public void setAdmin_pass(String admin_pass) {
		this.admin_pass = admin_pass;
	}
	public String getAdmin_name() {
		return admin_name;
	}
	public void setAdmin_name(String admin_name) {
		this.admin_name = admin_name;
	}
	public int getAdmin_grant() {
		return admin_grant;
	}
	public void setAdmin_grant(int admin_grant) {
		this.admin_grant = admin_grant;
	}
	
	
	// toString
	@Override
	public String toString() {
		return "AdminVO [admin_id=" + admin_id + ", admin_pass=" + admin_pass + ", admin_name=" + admin_name
				+ ", admin_grant=" + admin_grant + "]";
	}
	
	
	
}
