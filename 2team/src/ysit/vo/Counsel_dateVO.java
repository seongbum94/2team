package ysit.vo;

public class Counsel_dateVO {
/**
 * 교수가 상담신청 가능한 날짜를 띄우는 VO
 */
	
  private String pro_id; //교수 ID
  private String Imp_date; //상담가능일
  
  public static Counsel_dateVO conVO;
  
  //get , set
	public String getPro_id() {
		return pro_id;
	}

	public void setPro_id(String pro_id) {
		this.pro_id = pro_id;
	}

	public String getImp_date() {
		return Imp_date;
	}

	public void setImp_date(String imp_date) {
		Imp_date = imp_date;
	}
  
	//toString
	@Override
	public String toString() {
		return "Counsel_dateVO [pro_id=" + pro_id + ", Imp_date=" + Imp_date + "]";
	}
  
}
